class Calculadora:
    def sumar(self, a,b):
        return a + b
calc = Calculadora()
resultado = calc.sumar(10,5)
print(resultado) # Imprime: 15

class Calcculadora:
    def multiplicar(self, a,b):
        return a * b
calc = Calcculadora()
resultado = calc.multiplicar(10,5)
print(resultado) # Imprime: 50
